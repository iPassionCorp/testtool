package th.co.ipassion.testtool.dto.tlitext;

public class Ords {
    private String policyNo;
    private String accidentDate;
    private String receiveDate;
    private String riderType;
    private String riderSum;
    private String riderPay;
    private String payRate;
    private String causeType;
	public String getPolicyNo() {
		return policyNo;
	}
	public void setPolicyNo(String policyNo) {
		this.policyNo = policyNo;
	}
	public String getAccidentDate() {
		return accidentDate;
	}
	public void setAccidentDate(String accidentDate) {
		this.accidentDate = accidentDate;
	}
	public String getReceiveDate() {
		return receiveDate;
	}
	public void setReceiveDate(String receiveDate) {
		this.receiveDate = receiveDate;
	}
	public String getRiderType() {
		return riderType;
	}
	public void setRiderType(String riderType) {
		this.riderType = riderType;
	}
	public String getRiderSum() {
		return riderSum;
	}
	public void setRiderSum(String riderSum) {
		this.riderSum = riderSum;
	}
	public String getRiderPay() {
		return riderPay;
	}
	public void setRiderPay(String riderPay) {
		this.riderPay = riderPay;
	}
	public String getPayRate() {
		return payRate;
	}
	public void setPayRate(String payRate) {
		this.payRate = payRate;
	}
	public String getCauseType() {
		return causeType;
	}
	public void setCauseType(String causeType) {
		this.causeType = causeType;
	}
    
}
