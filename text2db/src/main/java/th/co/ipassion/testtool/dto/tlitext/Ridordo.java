package th.co.ipassion.testtool.dto.tlitext;

public class Ridordo {
    private String policyNo;
    private String receiveDate;
    private String riderType;
    private String riderAmount;

    public String getPolicyNo() {
        return policyNo;
    }

    public void setPolicyNo(String policyNo) {
        this.policyNo = policyNo;
    }

    public String getReceiveDate() {
        return receiveDate;
    }

    public void setReceiveDate(String receiveDate) {
        this.receiveDate = receiveDate;
    }

    public String getRiderType() {
        return riderType;
    }

    public void setRiderType(String riderType) {
        this.riderType = riderType;
    }

    public String getRiderAmount() {
        return riderAmount;
    }

    public void setRiderAmount(String riderAmount) {
        this.riderAmount = riderAmount;
    }
}
