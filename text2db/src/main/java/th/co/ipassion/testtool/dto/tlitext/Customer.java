package th.co.ipassion.testtool.dto.tlitext;

public class Customer {
    private String customerid;
    private String referenceid;
    private String referencetype;
    private String birthdate;
    private String sex;
    private String contactnameid;
    private String contactaddressid;
	public String getCustomerid() {
		return customerid;
	}
	public void setCustomerid(String customerid) {
		this.customerid = customerid;
	}
	public String getReferenceid() {
		return referenceid;
	}
	public void setReferenceid(String referenceid) {
		this.referenceid = referenceid;
	}
	public String getReferencetype() {
		return referencetype;
	}
	public void setReferencetype(String referencetype) {
		this.referencetype = referencetype;
	}
	public String getBirthdate() {
		return birthdate;
	}
	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getContactnameid() {
		return contactnameid;
	}
	public void setContactnameid(String contactnameid) {
		this.contactnameid = contactnameid;
	}
	public String getContactaddressid() {
		return contactaddressid;
	}
	public void setContactaddressid(String contactaddressid) {
		this.contactaddressid = contactaddressid;
	}
    
    
}
