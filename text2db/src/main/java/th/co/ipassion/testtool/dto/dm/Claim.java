package th.co.ipassion.testtool.dto.dm;

public class Claim {
    private String claimType;
    private String mstType;
    private String policyNo;
    private String receiveyyyymm;
    private String sectionCode;
    private String orderNo;
    private String referenceNo;
    private String okDate;

    public String getClaimType() {
        return claimType;
    }

    public void setClaimType(String claimType) {
        this.claimType = claimType;
    }

    public String getMstType() {
        return mstType;
    }

    public void setMstType(String mstType) {
        this.mstType = mstType;
    }

    public String getPolicyNo() {
        return policyNo;
    }

    public void setPolicyNo(String policyNo) {
        this.policyNo = policyNo;
    }

    public String getReceiveyyyymm() {
        return receiveyyyymm;
    }

    public void setReceiveyyyymm(String receiveyyyymm) {
        this.receiveyyyymm = receiveyyyymm;
    }

    public String getSectionCode() {
        return sectionCode;
    }

    public void setSectionCode(String sectionCode) {
        this.sectionCode = sectionCode;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public String getReferenceNo() {
        return referenceNo;
    }

    public void setReferenceNo(String referenceNo) {
        this.referenceNo = referenceNo;
    }

    public String getOkDate() {
        return okDate;
    }

    public void setOkDate(String okDate) {
        this.okDate = okDate;
    }
}
